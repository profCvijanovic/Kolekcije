package mape;

public class Artikal {
	
	private int sifraArtikla;
	private String imArtikla;
	private double cenaPoKg;
	private double tezina;
	
	public int getSifraArtikla() {
		return sifraArtikla;
	}
	public void setSifraArtikla(int sifraArtikla) {
		this.sifraArtikla = sifraArtikla;
	}
	public String getImArtikla() {
		return imArtikla;
	}
	public void setImArtikla(String imArtikla) {
		this.imArtikla = imArtikla;
	}
	public double getCenaPoKg() {
		return cenaPoKg;
	}
	public void setCenaPoKg(double cenaPoKg) {
		this.cenaPoKg = cenaPoKg;
	}
	public double getTezina() {
		return tezina;
	}
	public void setTezina(double tezina) {
		this.tezina = tezina;
	}
	
	

}
